function skillMember() {
  var member = document.getElementById("member");
  var skill = document.getElementById("skill");
  var memberText = document.getElementById("memberText");
  var skillText = document.getElementById("skillText");
  var memberImg = document.getElementById("memberImg");
  var skillImg = document.getElementById("skillImg");
  member.style.backgroundColor = "#fff";
  skill.style.backgroundColor = "#fff";
  memberText.style.color = "#000";
  skillText.style.color = "#000";
  memberImg.style.display = "block";
  skillImg.style.display = "none";
}